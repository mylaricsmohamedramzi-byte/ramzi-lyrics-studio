// Normalize and translate song title based on lang
export function translateTitle(title: string, lang: 'ar' | 'en'): string {
  if (lang === 'ar') return title;
  const key = title.trim();
  return TITLE_TRANSLATIONS[key] || title;
}

// Normalize and translate genre based on lang
export function translateGenre(genre: string, lang: 'ar' | 'en'): string {
  if (lang === 'ar') return genre;
  const key = genre.trim();
  return GENRE_TRANSLATIONS[key] || genre;
}
